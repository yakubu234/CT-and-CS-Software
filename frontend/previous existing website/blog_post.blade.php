                <div class="post-header">
                  <h4>Leave your Legacy through Planned Giving</h4>
                </div>
                <div class="post-meta">
                  <ul class="list-bordered-horizontal">
                    <li>
                      <dl class="list-terms-inline">
                        <dt>Date</dt>
                        <dd>
                          <time datetime="2018-01-22">January 22, 2018</time>
                        </dd>
                      </dl>
                    </li>
                    <li>
                      <dl class="list-terms-inline">
                        <dt>Posted by</dt>
                        <dd>Amelia Condon</dd>
                      </dl>
                    </li>
                    <li>
                      <dl class="list-terms-inline">
                        <dt>Comments</dt>
                        <dd>3</dd>
                      </dl>
                    </li>
                    <li>
                      <dl class="list-terms-inline">
