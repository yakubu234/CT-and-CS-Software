                        <dd>Wealth Management</dd>
                      </dl>
                    </li>
                  </ul>
                </div>
                <div class="divider-fullwidth bg-gray-light"></div>
                <div class="post-body">
                  <p>There comes a point in life where you want to begin sharing or gifting all the things you’ve collected over the years—stories, wisdom, financial wealth. And unlike the Ancient Egyptians believed, you cannot take your worldly goods with you when your light goes out. You can share your stories wisdom in a manifesto or through funny tales to your family, but what about the money?</p>
                  <p>Like most, you have (or should have) a will and most of your assets are likely going to friends and family. But, what about the causes and organizations you care about the 501(c)(3)s that depend on kind donations to continue doing the greatest good than you could do as an individual? Who is going to make your annual donation when you’re gone? Planned giving is the answer. Just like it sounds, planned giving is the act of creating a contribution of non-probate transfer vehicles (such as savings and checking accounts, investment funds, real estate), real property, or non-cash assets (like securities or retirement accounts), that is usually dispersed after an individual’s passing.</p>
                  <div class="quote-wrap-1">
                    <blockquote class="quote-minimal-bordered">
                      <p>
                        <q>Planned giving is an area of fundraising that refers to several specific gift types that can be funded with cash, equity, or property.</q>
                      </p>
                    </blockquote>
                  </div>
                  <p>Giving to the annual campaign is great and attending the big events is important, but there’s always this sense that your money isn’t going exactly where you would choose it to go. For example, you may be fully vested in one specific youth program at the local YMCA, but when you support the YMCA generally, your money is also going to other operations.</p>
